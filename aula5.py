# Tipo de dado bool (boolean)
# Ao questionar algo em um prograna,
# só existem duas respostas possiveis
# yes (True) or no (False) .
# Existem varios operadores para "questionar".
# Dentre eles o ==, que é um operador logico que
# questiona se um valor é igual ao outro

print(10 == 10) # True
print(10 > 11) # False
print(type(True)) # True
print(type(False)) # True
print(type(10 == 10)) # True
print(type(10 ==1)) # True
